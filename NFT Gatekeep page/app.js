const web3 = new Web3(Web3.givenProvider);
let account;
let aisenseNFT;
let aisenseNFTEnumerable;

async function initApp() {
    aisenseNFT = new web3.eth.Contract(ABI, CONTRACT_ADDRESS);
}

async function connectWallet() {
    if (typeof window.ethereum !== "undefined") {
        try {
            const accounts = await window.ethereum.request({ method: "eth_requestAccounts" });
            account = accounts[0];
            checkForValidNFT();
        } catch (error) {
            message.innerText = "Access to Metamask wallet denied";
            message.classList.remove("hidden");
        }
    } else {
        message.innerText = "Metamask is not installed";
        message.classList.remove("hidden");
    }
}

async function checkForValidNFT() {
    const balance = await aisenseNFT.methods.balanceOf(account).call();

    if (balance > 0) {
        const today = new Date();
        let validNFT = false;
        let tokenId = 1;

        while (!validNFT) {
            try {
                const tokenOwner = await aisenseNFT.methods.ownerOf(tokenId).call();

                if (tokenOwner.toLowerCase() === account.toLowerCase()) {
                    const tokenURI = await aisenseNFT.methods.tokenURI(tokenId).call();
                    const response = await fetch(tokenURI);
                    const metadata = await response.json();
                    const expirationDate = new Date(metadata.expiration);

                    if (expirationDate > today) {
                        validNFT = true;
                        break;
                    }
                }
            } catch (error) {
                // If the tokenId doesn't exist, break the loop
                if (error.message.includes("execution reverted")) {
                    break;
                }
            }

            tokenId++;
        }

        if (validNFT) {
            message.innerText = "Success! Thank you for verifying your NFT.";
            message.classList.remove("hidden");
            successLink.classList.remove("hidden");
        } else {
            message.innerText = "No valid NFTs have been detected in your wallet. Please make sure you have a valid NFT by purchasing one or staking your AIS tokens.";
            message.classList.remove("hidden");
        }
    } else {
        message.innerText = "No valid NFTs have been detected in your wallet. Please make sure you have a valid NFT by purchasing one or staking your AIS tokens.";
        message.classList.remove("hidden");
    }
}

connectMetamask.addEventListener("click", connectWallet);
initApp();